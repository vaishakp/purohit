"""Static webdir monitor publisher for Purohit rerun projects.

This module is designed for environments where inbound SSH tunneling or a
long-lived web server is inconvenient because access is mediated through
sshproxy, 2FA, or collaboration web authentication. Instead of exposing a Flask
server, a user-side process periodically writes static HTML and JSON files into
an existing web-accessible directory, such as the same webdir used by bilby_pipe
or PESummary outputs.
"""

